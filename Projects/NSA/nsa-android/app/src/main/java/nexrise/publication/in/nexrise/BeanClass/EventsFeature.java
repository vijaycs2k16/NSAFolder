package nexrise.publication.in.nexrise.BeanClass;

import java.io.Serializable;

/**
 * Created by karthik on 13-10-2016.
 */

public class EventsFeature implements Serializable{

    private String tag;
    private String title;
    private String shortDescription;
    private String description;
    private String date;
    private String time;
    private String duration;
    private String location;
    private String teacherName;
    private String attendees;

    public EventsFeature(String tag, String title,String shortDescription, String description, String date, String time, String duration, String location, String teacherName, String attendees) {
        this.tag = tag;
        this.title = title;
        this.shortDescription = shortDescription;
        this.description = description;
        this.date = date;
        this.time = time;
        this.duration = duration;
        this.location = location;
        this.teacherName = teacherName;
        this.attendees  = attendees;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getShortDescription() {
        return shortDescription;
    }

    public void setShortDescription(String shortDescription) {
        this.shortDescription = shortDescription;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getAttendees() {
        return attendees;
    }

    public void setAttendees(String attendees) {
        this.attendees = attendees;
    }
}
