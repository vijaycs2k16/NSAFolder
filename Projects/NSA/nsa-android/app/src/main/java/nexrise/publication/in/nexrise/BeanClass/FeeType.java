package nexrise.publication.in.nexrise.BeanClass;

import java.io.Serializable;

/**
 * Created by praga on 24-Feb-17.
 */

public class FeeType implements Serializable {

    private String name;
    private String amount;
    private int refundable;
    private String id;
    public FeeType(String name, String amount, int refundable, String id) {
        this.name = name;
        this.amount = amount;
        this.refundable = refundable;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public int getRefundable() {
        return refundable;
    }

    public void setRefundable(int refundable) {
        this.refundable = refundable;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
